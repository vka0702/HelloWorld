public class Calculator {


        public int summa ( int a, int b){
            int c = a + b ;
            return c;
        }

        public int raznica ( int a, int b){
            int c = a - b;
            return c;
        }

        public int umnozhenie ( int a, int b){
            int c = a * b;
            return c;
        }
}
